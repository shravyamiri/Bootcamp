# 6. Set Operations
a = {1, 2, 3}
b = {3, 4}

print("Set operations:")
print("Set A:", a)
print("Set B:", b)

# Intersection: elements common to both sets
print("   Intersection (A & B):", a & b)

# Union: all unique elements from both sets
print("   Union (A | B):", a | b)

# Difference: elements in A but not in B
print("   Difference (A - B):", a - b)
